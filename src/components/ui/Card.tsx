import * as React from 'react';

// A helper function for merging class names, which you'll likely need.
const cn = (...classes: string[]) => classes.filter(Boolean).join(' ');

export const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement>
>(({ className, ...props }, ref) => (
  <div
    ref={ref}
    className={cn(
      'rounded-lg border border-slate-700 bg-slate-800/50 shadow-lg paper-texture-dark',
      className
    )}
    {...props}
  />
));
Card.displayName = 'Card';

